import nonstandard as ns


def test_empty_bytes():
    assert ns.md5_bytes(b"") == b"\xd4\x1d\x8c\xd9\x8f\x00\xb2\x04\xe9\x80\x09\x98\xec\xf8\x42\x7e"


def test_basic_bytes():
    assert ns.md5_bytes(b"a948hgd") == b"\x62\xbd\x8a\xda\xf4\xab\x39\x4b\x10\xd5\x6e\xfa\xb0\x8b\x9a\x5e"


def test_empty_string():
    assert ns.md5_bytes("") == b"\xd4\x1d\x8c\xd9\x8f\x00\xb2\x04\xe9\x80\x09\x98\xec\xf8\x42\x7e"


def test_basic_string():
    assert ns.md5_bytes("a948hgd") == b"\x62\xbd\x8a\xda\xf4\xab\x39\x4b\x10\xd5\x6e\xfa\xb0\x8b\x9a\x5e"
